def sockMerchant(n, ar):
    # Write your code here
    c = Counter(ar)
    c1 = dict(c)
    pairs = 0
    for key,value in c1.items():
        if not value %2:
            pairi = value/2
        else:
            pairi = (value-1)/2
        pairs+=pairi

    return int(pairs)
    

